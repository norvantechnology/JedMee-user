/// Consistent spacing scale (4px base).
class AppSpacing {
  AppSpacing._();

  static const double xxs = 4;
  static const double xs = 8;
  static const double sm = 12;
  static const double md = 16;
  static const double lg = 20;
  static const double xl = 24;
  static const double xxl = 32;

  static const double pageHorizontalNarrow = 14;
  static const double pageHorizontalWide = 20;
  static const double cardPadding = 12;
  static const double listGap = 6;
  static const double sectionGap = 14;

  // ─── List row tokens ──────────────────────────────────────────────────────
  /// Vertical padding for all list rows.
  static const double rowPaddingV = 10;
  /// Horizontal padding for all list rows.
  static const double rowPaddingH = 14;

  // ─── Form tokens ──────────────────────────────────────────────────────────
  /// Gap between form field groups.
  static const double formGap = 10;
  /// Gap between form sections.
  static const double formSectionGap = 14;

  // ─── FAB clearance ────────────────────────────────────────────────────────
  /// Bottom padding for lists to ensure last row is visible above FAB.
  static const double fabClearance = 88;
}
